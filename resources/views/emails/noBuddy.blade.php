        <p>Hi,</p>
        <p>Your arrival is approaching and even though we try to do our best, unfortunately,
            we can not find a buddy for every exchange student. Buddy programme is a voluntary
            service and the number of exchange students coming every semester is very high.
            But don't worry!</p>
        <p>Our <a href="https://isc.cvut.cz/guide/orientation-week" target="_blank">Survival Guide</a>
            on our web site gives you all information and dates needed for the first days of your stay in Prague.</p>
        <p><a href="https://isc.cvut.cz/guide/welcome-pack" target="_blank">Welcome Packs</a>
            are prepared for you in our <a href="https://goo.gl/maps/MrjEDKdCTUm" target="_blank">ISC Point</a>
            in Masarykova dorm &ndash; it is very useful, because it includes confirmation of study
            (needed for student discount in public transport), map, info about registration
            at your new faculty, orientation week etc. Be sure to pick it up. <strong>The Welcome Pack
            is ready for you every day until Sunday, 10:00 - 22:00, Masarykova dormitory,
            room R304, 3rd floor (ISC Point).</strong></p>
        <p><strong>What do you need to do after your arrival:</strong></p>
        <ol>
            <li><strong>!!!!!Do not change the money at the airport!!!!!!</strong> It is not favorable. The best exchange agency is <a href="https://goo.gl/maps/buXiGkPoo3q" target="_blank">eXchange near Staroměstské náměstí</a>.</li>
            <li>How to get from the airport to Dejvcká:
                <ul>
                    <li>You need <strong>Czech money (CZK)</strong> to buy a ticket for a bus. If you don't have any, you need to use a change or withdraw money from ATM.</li>
                    <li>You can find a little shop with a big sign &quot;Public Transport&quot; at both terminals. Go there and buy a <strong>ticket for CZK 32</strong>. It is valid for 90 minutes since you validate it in the bus. That is enough for your way.</li>
                    <li>Find a station of <strong>bus No. 119</strong> (in front of the terminal). When you enter the bus, don't forget to mark the ticket in a yellow machine inside the bus.</li>
                    <li>Go to the terminal station &quot;<strong>Nádraží Veleslavín</strong>&quot; and change to the metro line A (green) in the direction of &quot;Depo Hostivař&quot;. Go to the station &quot;<strong>Dejvická</strong>&quot;.</li>
                </ul>
            </li>
            <li>Go to the ISC Point and get:
                <ul>
                    <li><a href="https://isc.cvut.cz/guide/welcome-pack">Welcome Pack</a></li>
                    <li><a href="https://isc.cvut.cz/guide/isc-esn">ESNcard &amp; SIM card</a></li>
                </ul>
            </li>
            <li>Go to the Identity Publishing House VIC and get:
                <ul>
                    <li><a href="https://isc.cvut.cz/guide/cards#transportation-card">Transportation card</a></li>
                    <li><a href="https://isc.cvut.cz/guide/cards">University ID card</a> and <a href="https://isc.cvut.cz/guide/kos">Login for the Study information system &ndash; KOS</a> (this is possible from Friday, 22 September)</li>
                </ul>
            </li>
        </ol>
        <p>In case you have any questions or difficulties, don't hesitate to contact us!</p>
        <p>Your ISC CTU in Prague</p>
