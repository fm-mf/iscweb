        <p>Hi,</p>
        <p>your arrival is approaching and even though we try to do our best, unfortunately,
            we can not find a buddy for every exchange student. Buddy program is a voluntary
            service and the number of exchange students coming every semester is very high. We keep you in the buddy database so anyone can still pick you as their buddy. The chances are low, though. 
            But don't worry!</p>
        <p>Our <a href="https://isc.cvut.cz/guide/orientation-week" target="_blank">Survival Guide</a>
            on our web site gives you all the information and dates needed for the first days of your stay in Prague.</p>
        <p><a href="https://isc.cvut.cz/guide/welcome-pack" target="_blank">Welcome Packs</a>
            are prepared for you in our <a href="https://goo.gl/maps/MrjEDKdCTUm" target="_blank">ISC Point</a>
            in Masarykova dorm &ndash; it is very useful because it includes confirmation of study
            (needed for student discount in public transport), info about registration
            at your new faculty, orientation week etc. Be sure to pick it up. <strong>The Welcome Pack
            is waiting for you at Masarykova dormitory, room R304, 3rd floor (ISC Point). Check our Facebook group to see if they are already ready to hand out.</strong></p>
        <p><strong>What do you need to do after your arrival:</strong></p>
        <ol>
            <li><strong>We do not recommend to exchange money at the airport.</strong> It is not favorable. As much as we know, the best exchange agency is eXchange <a href="https://goo.gl/maps/buXiGkPoo3q" target="_blank"> near Staroměstské náměstí</a> with a coupon available <a href="https://www.exchange.cz/kupon" target="_blank">here</a>.</li>
            <li>How to get from the airport to Dejvcká (where main CTU campus and Masarykova dormitory is):
                <ul>
                    <li>You can find a little shop with a big sign &quot;Public Transport&quot; at both terminals. Go there and buy a <strong>ticket for CZK 32</strong>. It is valid for 90 minutes since you validate it on the bus. That is enough for your way.</li>
                    <li>Another option is to buy the ticket from a yellow ticket machine at the airport, where you can use your credit card.</li>
                    <li>You can also buy the ticket in the PID Litacka app on your phone - install, register, buy the ticket with a credit card, activate ticket when you get in the bus. You need a working internet connection for this though.</li>
                    <li>Find a station of <strong>bus No. 119</strong> (in front of the terminal). When you enter the bus, don't forget to mark the ticket in a yellow machine inside the bus.</li>
                    <li>Go to the terminal station &quot;<strong>Nádraží Veleslavín</strong>&quot; and change to the metro line A (green) in the direction of &quot;Depo Hostivař&quot;. Go to the station &quot;<strong>Dejvická</strong>&quot;.</li>
                </ul>
            </li>
            <li>Go to the ISC Point and get:
                <ul>
                    <li><a href="https://isc.cvut.cz/guide/welcome-pack">Welcome Pack</a></li>
                    <li><a href="https://isc.cvut.cz/guide/isc-esn">ESNcard &amp; SIM card &amp; T-shirt</a> (ISC Membership)</li>
                </ul>
            </li>
            <li> Go to the metro/subway station Hradčanská or Nádraží Veleslavín and get:
             <ul>
                    <li>3month student public transport pass (renew for another 3 months once it expires, don't forget the confirmation of study from your Welcome Pack and a passport photo)</li>
             </ul>

            
            </li>
            <li>Go to the Identity Publishing House VIC and get:
                <ul>
                    <li><a href="https://isc.cvut.cz/guide/cards">University ID card</a> and <a href="https://isc.cvut.cz/guide/kos">Login for the Study information system &ndash; KOS</a> (check when it is possible at your printed Survival Guide)</li>
                </ul>
            </li>
        </ol>
        <p>In case you have any questions or difficulties, don't hesitate to contact us!</p>
