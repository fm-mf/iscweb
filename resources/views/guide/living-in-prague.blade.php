    <h2>Living in Prague</h2>

    <h3>Customs and traditions</h3>
    <ul>
        <li>Easter (Spring)</li>
        <li>Burning of Witches (30 April)</li>
        <li>Lover's Day (1st May)</li>
        <li>St. Nicholas's Day (5th December)</li>
        <li>Christmas (December)</li>
    </ul>
    <p>See <a href="https://www.facebook.com/pg/isc.ctu.prague/events/" target="_blank">ISC Facebook page events</a> to get to know Czech traditions</p>

    <h3>Local Cuisine</h3>
    <ul>
        <li>Similar to German and Austrian</li>
        <li>Typically: meat + side dish (potatoes/dumplings) + sauce</li>
        <li>Czech specialities:
            <dl>
                <dt>vepřo-knedlo-zelo</dt>
                <dd>(sautéd pork-cabbage-dumplings)</dd>
                <dt>svíčková</dt>
                <dd>beef sirloin in gravy (creamy vegetable sauce) with dumplings, served with cranberries and whipped cream</dd>
                <dt>bramboráky</dt>
                <dd>garlic-seasoned fried potato pancakes</dd>
                <dt>smažený sýr</dt>
                <dd>fried cheese</dd>
                <dt>nakládaný hermelín</dt>
                <dd>pickled brie cheese</dd>
                <dt>koláče</dt>
                <dd>small pastries topped with fruit or poppy-seed paste</dd>
            </dl>
        </li>
        <li>Typical: Pork schnitzel, game (deer, wild boar, rabbit, freshwater fish such as trout), goulash (served with dumplings or as a soup)</li>
        <li>Farmers’ Markets (&quot;Farmářské trhy&quot;)
            <ul>
                <li>Held in spring and fall</li>
                <li>products from local farmers, much of it organic (“bio”)</li>
                <li>Most of them are open on Saturdays (e.g. at Vítězné námestí, Praha 6)</li>
            </ul>
        </li>
    </ul>

    <h3>Drinks</h3>

    <h4>Beer (pivo)</h4>
    <ul>
        <li>Is recognized as world's best</li>
        <li>Over 60 breweries (Pilsner Urquell, Budvar, Velkopopovický kozel, Staropramen (Prague))</li>
        <li>part of the Czech lifestyle</li>
    </ul>

    <h4>Wine</h4>
    <ul>
        <li>Moravia</li>
        <li>taking top honors at competitions around Central Europe</li>
        <li>Burčák - grape juice that separates from the crop as it is pressed, season: mid August till the end of November, try it at one of the annual wine festivals in September (&quot;Vinobraní&quot;)</li>
    </ul>

    <h4>Slivovice</h4>
    <ul>
        <li>Plum brandy (strong spirit)</li>
        <li>South Moravia</li>
    </ul>

    <h4>Becherovka</h4>
    <ul>
        <li>Typical Czech herbal liquor</li>
        <li>Karlovy Vary</li>
    </ul>

    <h3>Tipping</h3>
    <ul>
        <li>5-10% (in any restaurant with table service)</li>
        <li>The usual system: the waiter announces the amount as he hands over the bill and customers announce how much they wish to pay (with the tip included) while settling their tab.</li>
        <li>It is usual to tip hairdressers, barbers and people providing other services.</li>
    </ul>

    <h3>Hiking</h3>
    <ul>
        <li>Long-lasting tradition in the Czech Republic</li>
        <li>Czech Hiking Club (Klub českých turistů) has made coloured signs to mark hiking trails</li>
        <li>Generally: blue - longer main trails, yellow and green connect them</li>
        <li>Over 40 000 km, annually updated</li>
        <li>You can see them at <a href="https://mapy.cz/turisticka" target="_blank">Mapy.cz</a> (the best maps for the Czech Republic), download an app Mapy.cz <a href="https://play.google.com/store/apps/details?id=cz.seznam.mapy" target="_blank">for Android</a>, <a href="https://itunes.apple.com/cz/app/mapy-cz/id411411020" target="_blank">for iOS</a> or <a href="https://www.microsoft.com/cs-cz/store/p/mapycz/9wzdncrfhzdc" target="_blank">for Windows Phone</a> (the app allows you to save the map for offline usage) or buy paper maps in bookstores</li>
        <li>See <a href="https://www.facebook.com/pg/isc.ctu.prague/events/" target="_blank">ISC Facebook page events</a> to join organized hiking trips</li>
    </ul>

