            <p>Some of the symptoms of culture shock include
                excessive concern over cleanliness and the feeling
                that what is new and strange is “dirty”. There can
                be a feeling of helplessness, irritation over delays,
                and other minor frustrations out of proportion
                to their causes. Further symptoms may be a feeling  that  the  “natives”  are  really  perverse,  and
                their  social  rules  deserve  no  respect.  This  may
                be  accompanied  by  unwillingness  or  refusal  to
                learn the language of the host country. In addition, there can be excessive fear of being cheated,
                robbed, or injured, and great concern over minor
                pains. Last but not least, there may be a terrible
                longing to be back home, in familiar surroundings, with friends and relatives, and, in general, to
                talk to people who really “make sense”.</p>
            <p>Most  likely,  you  will  experience  some  level
                of  culture  shock.  After  all,  it  is  normal  to  miss
                the  comforts  and  familiarity  of  home.  In  the
                course  of  one  of  your  first  extended  stays
                abroad,  you  will  have  to  learn  how  to  deal
                with it.</p>
            <p>If  you  are  getting  low,  do  seek  help.  The
                International  Office  and  the  International
                Student  Club  offer  a  sympathetic  ear  -  talking
                it  out  at  length,  with  a  “native”,  can  be  very
                therapeutic.</p>
            <p>In exceptional cases, culture shock can make
                people dangerously depressed. Our normal reaction to dangerously depressed people is to ignore
